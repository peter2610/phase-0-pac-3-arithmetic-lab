// 1. Adds two numbers and returns the result
function add(a, b) {
  return a + b;
}

// 2. Subtracts the second number from the first
function subtract(a, b) {
  return a - b;
}

// 3. Multiplies two numbers
function multiply(a, b) {
  return a * b;
}

// 4. Divides the first number by the second
function divide(a, b) {
  return a / b;
}

// 5. Increments a number by 1
function increment(n) {
  return n + 1;
}

// 6. Decrements a number by 1
function decrement(n) {
  return n - 1;
}

// 7. Parses a string and returns an integer using base 10
function makeInt(string) {
  return parseInt(string, 10);
}

// 8. Parses a string and returns a floating-point number
function preserveDecimal(string) {
  return parseFloat(string);
}
